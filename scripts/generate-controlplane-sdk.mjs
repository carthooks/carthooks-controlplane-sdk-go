const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..')
const contractPath = path.join(root, 'contracts', 'controlplane_swagger.json')
const outDir = path.join(root, 'controlplane')
const swagger = JSON.parse(fs.readFileSync(contractPath, 'utf8'))
const definitions = swagger.definitions || {}
const paths = swagger.paths || {}

const dtoNames = Object.keys(definitions)
  .filter((name) => name.startsWith('vo.'))
  .sort()

const initialisms = new Set(['id', 'url', 'api'])
const timeFields = new Map([
  ['expiresAt', 'time.Time'],
  ['profileSyncedAt', '*time.Time'],
])

function fail(message) {
  console.error(message)
  process.exit(1)
}

function goTypeName(ref) {
  const name = ref.replace(/^#\/definitions\//, '').replace(/^vo\./, '')
  if (!name || name.startsWith('pin.')) {
    fail(`unsupported ref: ${ref}`)
  }
  return name
}

function fieldName(jsonName) {
  return jsonName
    .replace(/_([a-zA-Z0-9])/g, (_, c) => c.toUpperCase())
    .replace(/^[a-z]/, (c) => c.toUpperCase())
    .replace(/Ids\b/g, 'IDs')
    .replace(/Id\b/g, 'ID')
    .replace(/Url\b/g, 'URL')
    .replace(/Api\b/g, 'API')
    .split(/(?=[A-Z])/)
    .map((part) => initialisms.has(part.toLowerCase()) ? part.toUpperCase() : part)
    .join('')
}

function schemaType(schema, jsonName = '') {
  if (!schema) {
    fail(`missing schema for ${jsonName}`)
  }
  if (schema.$ref) {
    return goTypeName(schema.$ref)
  }
  if (timeFields.has(jsonName)) {
    return timeFields.get(jsonName)
  }
  switch (schema.type) {
    case 'string':
      return 'string'
    case 'boolean':
      return 'bool'
    case 'integer':
      return 'int'
    case 'number':
      return 'float64'
    case 'array':
      return `[]${schemaType(schema.items, jsonName)}`
    case 'object':
      return 'map[string]any'
    default:
      fail(`unsupported schema type for ${jsonName}: ${JSON.stringify(schema)}`)
  }
}

function generateTypes() {
  let needsTime = false
  const chunks = []
  chunks.push('type TenantStatusRequest struct {')
  chunks.push('\tExternalSource string `json:"externalSource" form:"externalSource"`')
  chunks.push('\tExternalSubjectType string `json:"externalSubjectType" form:"externalSubjectType"`')
  chunks.push('\tExternalSubjectID string `json:"externalSubjectId" form:"externalSubjectId"`')
  chunks.push('}\n')

  for (const fullName of dtoNames) {
    const name = fullName.replace(/^vo\./, '')
    const def = definitions[fullName]
    const props = def.properties || {}
    const propNames = Object.keys(props).sort()
    chunks.push(`type ${name} struct {`)
    for (const propName of propNames) {
      const typ = schemaType(props[propName], propName)
      if (typ.includes('time.')) {
        needsTime = true
      }
      chunks.push(`\t${fieldName(propName)} ${typ} \`json:"${propName}"\``)
    }
    chunks.push('}\n')
  }

  return `// Code generated by scripts/generate-controlplane-sdk.mjs; DO NOT EDIT.\n\npackage controlplane\n\n${needsTime ? 'import "time"\n\n' : ''}${chunks.join('\n')}`
}

function successDataRef(pathName, method) {
  const op = paths[pathName]?.[method]
  if (!op) {
    fail(`missing swagger operation: ${method.toUpperCase()} ${pathName}`)
  }
  const success = op.responses?.['200']
  const dataRef = success?.schema?.properties?.data?.$ref
  if (dataRef) {
    return goTypeName(dataRef)
  }
  const dataType = success?.schema?.properties?.data?.type
  if (dataType === 'boolean') {
    return 'bool'
  }
  fail(`missing success data ref: ${method.toUpperCase()} ${pathName}`)
}

