const httpMethods = {
  get: 'http.MethodGet',
  post: 'http.MethodPost',
  put: 'http.MethodPut',
  delete: 'http.MethodDelete',
}

function generateClient() {
  const chunks = []
  for (const op of operations) {
    const swaggerOp = paths[op.path]?.[op.method]
    if (!swaggerOp) {
      fail(`missing swagger operation: ${op.method.toUpperCase()} ${op.path}`)
    }
    const respType = op.response || successDataRef(op.path, op.method)
    const pathExpr = op.pathExpr || JSON.stringify(op.path)
    chunks.push(`func (c *Client) ${op.name}(ctx context.Context, ${op.args}) ${op.returnsErrorOnly ? 'error' : `(*${respType}, error)`} {`)
    if (op.query?.length) {
      chunks.push('\tparams := url.Values{}')
      for (const [key, value] of op.query) {
        chunks.push(`\tparams.Set("${key}", strings.TrimSpace(${value}))`)
      }
    }
    if (op.returnsErrorOnly) {
      chunks.push(`\t_, err := doJSON[${op.request === 'struct{}{}' ? 'struct{}' : 'any'}, ${respType}](ctx, c, ${httpMethods[op.method]}, ${pathExpr}, ${op.request}, ${op.query?.length ? 'params' : 'nil'})`)
      chunks.push('\treturn err')
    } else {
      const reqType = op.request === 'struct{}{}' ? 'struct{}' : op.request.replace(/^req$/, op.args.match(/req ([A-Za-z0-9]+)/)?.[1] || 'any')
      chunks.push(`\treturn doJSON[${reqType}, ${respType}](ctx, c, ${httpMethods[op.method]}, ${pathExpr}, ${op.request}, ${op.query?.length ? 'params' : 'nil'})`)
    }
    chunks.push('}\n')
  }
  return `// Code generated by scripts/generate-controlplane-sdk.mjs; DO NOT EDIT.\n\npackage controlplane\n\nimport (\n\t"context"\n\t"fmt"\n\t"net/http"\n\t"net/url"\n\t"strings"\n)\n\n${chunks.join('\n')}`
}

fs.writeFileSync(path.join(outDir, 'types_generated.go'), generateTypes())
fs.writeFileSync(path.join(outDir, 'client_generated.go'), generateClient())
console.log('Generated controlplane/types_generated.go')
console.log('Generated controlplane/client_generated.go')
